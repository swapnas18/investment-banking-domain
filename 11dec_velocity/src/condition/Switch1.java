package condition;

public class Switch1 {

	public static void main(String[] args) {
		
			int day=3;
		switch (day) {
		case 1:
			System.out.println( " i am sunday ");
			break;
		case 2:
			System.out.println( " i am monday ");
			break;
		case 3:
			System.out.println( " i am Tuesaday ");
			break;
		case 4:
			System.out.println( " i am wendsday ");
			break;
		case 5:
			System.out.println( " i am thursday ");
			break;
		case 6:
			System.out.println( " i am friday ");
			break;
		case 7:
			System.out.println( " i am saturday");
			break;

		default:
			
			System.out.println("only 7 days are in one week");
			break;
		}
		}
	

}
